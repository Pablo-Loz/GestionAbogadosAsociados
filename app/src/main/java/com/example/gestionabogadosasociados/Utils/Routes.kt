package com.example.gestionabogadosasociados
/**
 * Enum que define las diferentes rutas utilizadas en la navegación de la app.
 *
 * @property value El título asociado con la ruta.
 */
enum class Routes(val value: String) {
    Login("Acceso Al Servidor"),
    Webview("Abogados Asociados"),
    Exit("Salir de la APP")
}

